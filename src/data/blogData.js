// Helper functions
export const getFeaturedPosts = () => blogPosts.filter(post => post.featured);
export const getPostBySlug = (slug) => blogPosts.find(post => post.slug === slug);
export const getPostById = (id) => blogPosts.find(post => post.id === parseInt(id));
export const getPostsByCategory = (category) => blogPosts.filter(post => post.category === category);
export const getRelatedPosts = (currentPostId, category, limit = 3) => {
  return blogPosts
    .filter(post => post.id !== currentPostId && post.category === category)
    .slice(0, limit);
};

// Get all unique categories
export const getCategories = () => {
  const categories = blogPosts.map(post => post.category);
  return [...new Set(categories)];
};

// Get all unique tags
export const getTags = () => {
  const tags = blogPosts.flatMap(post => post.tags);
  return [...new Set(tags)];
};